                    <h2 class="sub-page-heading">IQAC</h2>
                        <p>Way back in 2015 our senior teachers started to feel that an institution that really understand its strength, weakness potentials and limitations is likely to be more effective in carrying out its educational mission and it was from this thinking that they realized the need to construct an IQAC set up. If quality required to be assured, we need a structured Quality Assurance cell and system that can make it possible to monitor, improve and evaluate quality. The IQAC of Soghra College has been formulated as per guidelines by NAAC and is constantly striving to enhance the quality of all section of the college.


                        <br><br>
                        <strong class="theme-text">IQAC Functions - As per UGC Guidelines:</strong>
                        <ul>
                            <li>Development and application of quality benchmarks/parameters for the various academic and administrative activities of the Colleges;</li>
                            <li>Facilitating the creation of a learner-centric environment conducive for quality education and faculty maturation to adopt the required knowledge and        technology for participatory teaching and learning process </li>
                            <li>Arrangement for feedback responses from students, parents and other stakeholders on quality-related institutional processes</li>
                            <li>Dissemination of information on the various quality parameters of higher education.</li>
                            <li>Organization of inter and intra institutional workshops, seminars on quality related themes and promotion of quality circles. </li>
                            <li>Documentation of the various programmes/activities of the College, leading to quality improvement.</li>
                        </ul>
                        </p>
